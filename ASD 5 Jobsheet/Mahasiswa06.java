// class ini berfungsi untuk menyimpan data setiap mahasiswa

public class Mahasiswa06 {
    public String nama;
    public int nim;
    public int tahunMasuk;
    public int nilaiUTS;
    public int nilaiUAS;

    public Mahasiswa06(String nama, int nim, int tahunMasuk, int nilaiUTS, int nilaiUAS) {
        this.nama = nama;
        this.nim = nim;
        this.tahunMasuk = tahunMasuk;
        this.nilaiUTS = nilaiUTS;
        this.nilaiUAS = nilaiUAS;
    }
}